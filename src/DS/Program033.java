/**
 * Demonstrates conditional statements - Example 33
 */
public class Program033 {
    public static void main(String[] args) {
        int value = 33;
        if (value > 50) {
            System.out.println(value + " is greater than 50");
        } else {
            System.out.println(value + " is less than or equal to 50");
        }
    }
}
