package Streams;

/**
 * Demonstrates array operations - Example 4
 */
public class Program004 {
    public static void main(String[] args) {
        int[] arr = {4, 5, 6, 7, 8};
        System.out.println("Array length: " + arr.length);
        for (int i : arr) {
            System.out.println("Element: " + i);
        }
    }
}
