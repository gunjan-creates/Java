package Streams;

/**
 * Demonstrates conditional statements - Example 3
 */
public class Program003 {
    public static void main(String[] args) {
        int value = 3;
        if (value > 50) {
            System.out.println(value + " is greater than 50");
        } else {
            System.out.println(value + " is less than or equal to 50");
        }
    }
}
